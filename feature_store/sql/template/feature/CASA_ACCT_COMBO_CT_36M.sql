INSERT INTO {TBL_NM}
SELECT
  DIM.CUSTOMER_CDE,
  'CASA_ACCT_COMBO_CT_36M' FTR_NM,
  COUNT(DISTINCT DIM.ACCT_ID) AS FTR_VAL,
  TO_DATE('{RPT_DT}', 'DD-MM-YY') AS RPT_DT,
  CURRENT_TIMESTAMP AS ADD_TSTP
FROM DW_ANALYTICS.DW_ACCOUNT_MASTER_DIM DIM
JOIN DW_ANALYTICS.DWA_STMT_EBANK FCT ON DIM.CUSTOMER_CDE = FCT.CUSTOMER_ID
JOIN DW_ANALYTICS.TRANSACTION_CODE TC ON FCT.TRANSACTION_CODE = TC.TRANSACTION_CODE
JOIN DW_ANALYTICS.DWL_PRODUCT_MASTER_DIM PM ON FCT.PRODUCT_CATEGORY = PM.CATEGORY_CDE
WHERE DIM.ACTIVE = 1
  AND DIM.ACCT_ID = FCT.ACCOUNT_NUMBER
  AND TC.INITIATION = 'CUSTOMER'
  AND DIM.CATEGORY_CDE LIKE '10__'
  AND PM.CHARGE_CDE IN ('1001','1008','1009','1011','1012','1013','1014','1015','1016','1018','1020','1021','1022','1023','1024','1025','1026','1027','1028','1029','1033','1034','1035','1036','1037','1038','1041','1042','1043','1044','1045','1046','1047','1049','1050','1060','1061','1062','1063','1064','1066','1069','1070','1071','1090','1112','1113','1114','1115','1116','1120','1121','1122','1123','1124','1125','1126','1127','1128','1129','1133','1134','1135','1136','1137','1138','1141','1142','1143','1144','1145','1146','1147','1149','1150','1160','1161','1162','1163','1164','1169','1170','1171','1247','1248','1249','1250','2047','2048','2049','2050')
  AND FCT.PROCESS_DT < TO_DATE('{RPT_DT}', 'DD-MM-YY')
  AND FCT.PROCESS_DT >= ADD_MONTHS(TO_DATE('{RPT_DT}', 'DD-MM-YY'), -36)
GROUP BY DIM.CUSTOMER_CDE;